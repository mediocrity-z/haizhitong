exports.main = async () => {
  const now = Date.now();
  const expireAt = now + CACHE_TTL;
  let inserted = 0;
  let skipped = 0;

  for (const [country, result] of Object.entries(VISA_DATA)) {
    // 检查是否已有有效缓存
    const exist = await db
      .collection("visa_cache")
      .where({ country })
      .get();

    if (exist.data.length > 0 && exist.data[0].expireAt > now) {
      skipped++;
      continue;
    }

    // 删除过期缓存
    for (const old of exist.data) {
      await db.collection("visa_cache").doc(old._id).remove();
    }

    // 写入新缓存
    await db.collection("visa_cache").add({
      data: { country, result, createTime: now, expireAt },
    });
    inserted++;
  }

  return {
    code: 0,
    msg: `预填充完成：新增 ${inserted} 条，跳过 ${skipped} 条（已有有效缓存）`,
  };
};
